public class CondtionalMain2 {

	public static void main(String[] args) {
		System.out.println("?");
		int num =1;
		switch(num) {
		case 0 :
			System.out.println("수강등록");
			break;
		case 1 :
			System.out.println("기초 프로그래밍");
			break;
		case 2 :
			System.out.println("취업 관련");
			break;
		case 3 :
			System.out.println("종료");
			break;
	
		}

	}

}
